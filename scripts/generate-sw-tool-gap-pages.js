function repairExisting(page, file) {
  const enUrl = `${SITE}/tools/${page.enSlug}/`;
  const swUrl = `${SITE}/sw/zana/${page.swSlug}/`;
  let html = fs.readFileSync(file, "utf8");
  html = html.replace(/<html\b([^>]*)\blang=["'][^"']*["']([^>]*)>/i, '<html$1lang="sw"$2>');
  html = ensureMeta(html, "content-language", "sw");
  html = ensureMeta(html, "afrotools-content-id", `sw-tool-gap:${page.swSlug}`);
  html = ensureMeta(html, "afrotools-source-page", `tools/${page.enSlug}`);
  html = upsertAlternate(html, "en", enUrl);
  html = upsertAlternate(html, "sw", swUrl);
  html = upsertAlternate(html, "x-default", enUrl);
  html = ensureAccessibilityRuntime(html);
  writeAtomic(file, html);
}

function updateEnglishHreflang(page) {
  const file = path.join(ROOT, "tools", page.enSlug, "index.html");
  const enUrl = `${SITE}/tools/${page.enSlug}/`;
  const swUrl = `${SITE}/sw/zana/${page.swSlug}/`;
  let html = fs.readFileSync(file, "utf8");
  const before = html;
  html = upsertAlternate(html, "en", enUrl);
  html = upsertAlternate(html, "sw", swUrl);
  html = upsertAlternate(html, "x-default", enUrl);
  if (html !== before) writeAtomic(file, html);
  return html !== before;
}

function selectedPages() {
  const slugArg = process.argv.find((arg) => arg.startsWith("--slugs="));
  if (!slugArg) return WAVE.swahili;
  const selected = new Set(slugArg.slice("--slugs=".length).split(",").map((slug) => slug.trim()).filter(Boolean));
  const pages = WAVE.swahili.filter((page) => selected.has(page.enSlug) || selected.has(page.swSlug));
  if (pages.length !== selected.size) throw new Error("Unknown or duplicate Swahili wave slug in --slugs");
  return pages;
}

function main() {
  let generated = 0;
  let reused = 0;
  let englishUpdated = 0;
  for (const page of selectedPages()) {
    const source = path.join(ROOT, "tools", page.enSlug, "index.html");
    if (!fs.existsSync(source)) throw new Error(`Missing English source: tools/${page.enSlug}/index.html`);
    const output = path.join(ROOT, "sw", "zana", page.swSlug, "index.html");
    if (fs.existsSync(output) && page.reuseExisting) {
      repairExisting(page, output);
      reused += 1;
    } else {
      if (fs.existsSync(output) && !fs.readFileSync(output, "utf8").includes(GENERATED_MARKER)) {
        throw new Error(`Swahili route collision: sw/zana/${page.swSlug}/index.html`);
      }
      writeAtomic(output, htmlFor(page));
      generated += 1;
    }
    if (updateEnglishHreflang(page)) englishUpdated += 1;
  }
  console.log(`Swahili coverage wave: ${generated} generated, ${reused} existing native routes mapped, ${englishUpdated} English hreflang sources updated.`);
}

main();
