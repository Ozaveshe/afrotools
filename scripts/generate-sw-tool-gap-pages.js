const fs = require("fs");
const path = require("path");
const {
  writeFileSyncWithRetry,
  renameSyncWithRetry,
  unlinkSyncWithRetry,
} = require("./lib/safe-write");

const ROOT = path.resolve(__dirname, "..");
const SITE = "https://afrotools.com";
const WAVE = require("../data/localization/coverage-wave-2026-07.json");
const GENERATED_MARKER = "Generated by scripts/generate-sw-tool-gap-pages.js";

function escapeHtml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

function writeAtomic(file, value) {
  fs.mkdirSync(path.dirname(file), { recursive: true });
  const temp = `${file}.tmp-${process.pid}`;
  unlinkSyncWithRetry(temp);
  writeFileSyncWithRetry(temp, value.normalize("NFC").replace(/\r\n/g, "\n"), "utf8");
  if (fs.existsSync(file)) unlinkSyncWithRetry(file);
  renameSyncWithRetry(temp, file);
}

function upsertAlternate(html, lang, href) {
  const tag = `<link rel="alternate" hreflang="${lang}" href="${href}">`;
  const pattern = new RegExp(`<link\\b(?=[^>]*\\brel=["']alternate["'])(?=[^>]*\\bhreflang=["']${lang.replace(/[-/\\^$*+?.()|[\]{}]/g, "\\$&")}["'])[^>]*>`, "i");
  if (pattern.test(html)) return html.replace(pattern, tag);
  const canonical = html.match(/<link\b(?=[^>]*\brel=["']canonical["'])[^>]*>\s*/i);
  if (canonical) return html.replace(canonical[0], `${canonical[0]}${tag}\n`);
  return html.replace(/<\/head>/i, `  ${tag}\n</head>`);
}

function ensureMeta(html, name, content) {
  const tag = `<meta name="${name}" content="${escapeHtml(content)}">`;
  const pattern = new RegExp(`<meta\\b(?=[^>]*\\bname=["']${name.replace(/[-/\\^$*+?.()|[\]{}]/g, "\\$&")}["'])[^>]*>`, "i");
  if (pattern.test(html)) return html.replace(pattern, tag);
  return html.replace(/<\/head>/i, `  ${tag}\n</head>`);
}

function ensureAccessibilityRuntime(html) {
  const script = '<script src="/assets/js/lib/sw-accessibility.js" defer></script>';
  if (html.includes(script)) return html;
  return html.replace(/<\/body>/i, `${script}\n</body>`);
}

