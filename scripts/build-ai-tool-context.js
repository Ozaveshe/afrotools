function sentenceParts(text) {
  return String(text).match(/[^.!?]+[.!?]+(?:\s+|$)|[^.!?]+$/g) || [String(text)];
}

function trimJoin(parts) {
  return parts
    .map(function (part) {
      return part.trim();
    })
    .filter(Boolean)
    .join(' ');
}

function firstSentence(text) {
  return trimJoin(sentenceParts(text).slice(0, 1));
}

function definitionFromLegacy(toolKey, text) {
  const binding = sourceBindingForKey(toolKey);
  const base = { schemaVersion: 1, toolKey, legacyTextSha256: sha256(text) };

  if (!binding) {
    return Object.assign(base, { status: 'unverified-static', staticText: text });
  }

  const parts = sentenceParts(text);
  let staticParts = [];
  let unverifiedParts = [];

  if (binding.kind === 'paye-engine') {
    staticParts.push(firstSentence(text));
    const languageInstruction = parts.find(function (part) {
      return /IMPORTANT:|TOUJOURS en fran[cç]ais|always respond in french/i.test(part);
    });
    if (languageInstruction && !/\d/.test(languageInstruction)) staticParts.push(languageInstruction);
  } else if (binding.kind === 'vat-formula') {
    staticParts.push(firstSentence(text));
    parts.slice(1).forEach(function (part) {
      if (/\d/.test(part)) unverifiedParts.push(part);
      else staticParts.push(part);
    });
    // The first numeric sentence is the standard rate already owned by the
    // calculator artifact. Remove it from the unverified legacy remainder.
    if (unverifiedParts.length) unverifiedParts.shift();
  } else if (binding.kind === 'policy-rate-snapshot' || binding.kind === 'forex-snapshot') {
    parts.forEach(function (part) {
      if (!/\d/.test(part)) staticParts.push(part);
    });
  } else {
    parts.forEach(function (part) {
      if (/\d/.test(part)) unverifiedParts.push(part);
      else staticParts.push(part);
    });
  }

  // A source-coupled definition never keeps legacy numeric facts. The
  // legacy hash proves which prompt was migrated, while the builder below
  // regenerates all numbers from the declared binding. Numeric prose is
  // retained only in definitions whose entire status is unverified-static.
  unverifiedParts = [];

  const definition = Object.assign(base, { status: 'source-coupled', staticText: trimJoin(staticParts), sourceBindings: [binding] });
  if (unverifiedParts.length) {
    definition.unverifiedStaticBlocks = [{ status: 'unverified-static', text: trimJoin(unverifiedParts) }];
  }
  return definition;
}

function bootstrapDefinitions() {
  const contexts = extractInlineContexts();
  const keys = Object.keys(contexts).sort();
  if (!keys.length) throw new Error('Inline TOOL_CONTEXT is empty');
  for (const toolKey of keys) {
    if (!/^[a-z0-9-]+$/.test(toolKey)) throw new Error('Unsafe tool key: ' + toolKey);
    const definition = definitionFromLegacy(toolKey, contexts[toolKey]);
    atomicWrite(path.join(CONTEXT_DIR, toolKey + '.json'), stableJson(definition));
  }
  return keys.length;
}

function rewriteAdvisorImport() {
  const source = fs.readFileSync(ADVISOR_PATH, 'utf8');
  const blockStart = source.indexOf('// Per-country/tool context injected into system prompt');
  const blockEnd = source.indexOf('// Tool affinity map', blockStart);
  if (blockStart < 0 || blockEnd < 0) {
    throw new Error('Inline TOOL_CONTEXT block not found; advisor was not rewritten.');
  }
  const replacement = "const TOOL_CONTEXT = require('./_shared/ai-tool-context.generated.js');\n\n";
  atomicWrite(ADVISOR_PATH, source.slice(0, blockStart) + replacement + source.slice(blockEnd));
}

function validateDefinition(definition, filePath) {
  if (!definition || definition.schemaVersion !== 1) throw new Error(filePath + ': schemaVersion must be 1');
  if (!/^[a-z0-9-]+$/.test(definition.toolKey || '')) throw new Error(filePath + ': invalid toolKey');
  if (!['source-coupled', 'unverified-static'].includes(definition.status)) throw new Error(filePath + ': invalid status');
  if (typeof definition.staticText !== 'string') throw new Error(filePath + ': staticText must be a string');
  if (!/^sha256:[a-f0-9]{64}$/.test(definition.legacyTextSha256 || '')) throw new Error(filePath + ': invalid legacyTextSha256');
  if (definition.status === 'source-coupled') {
    if (!Array.isArray(definition.sourceBindings) || definition.sourceBindings.length !== 1) {
      throw new Error(filePath + ': source-coupled contexts require one source binding');
    }
    if (/\d/.test(definition.staticText)) {
      throw new Error(filePath + ': hand-typed numeric facts must be in an unverified-static block');
    }
  }
  for (const block of definition.unverifiedStaticBlocks || []) {
    if (block.status !== 'unverified-static' || typeof block.text !== 'string') {
      throw new Error(filePath + ': invalid unverified-static block');
    }
  }
}

function loadDefinitions() {
  if (!fs.existsSync(CONTEXT_DIR)) throw new Error('Missing ' + path.relative(ROOT, CONTEXT_DIR));
  const files = fs
    .readdirSync(CONTEXT_DIR)
    .filter(function (name) {
      return name.endsWith('.json');
    })
    .sort();
  const definitions = [];
  for (const fileName of files) {
    const filePath = path.join(CONTEXT_DIR, fileName);
    const definition = readJson(filePath);
    validateDefinition(definition, path.relative(ROOT, filePath));
    if (fileName !== definition.toolKey + '.json') throw new Error(fileName + ': filename must match toolKey');
    definitions.push(definition);
  }
  const duplicate = definitions.find(function (definition, index) {
    return (
      definitions.findIndex(function (candidate) {
        return candidate.toolKey === definition.toolKey;
      }) !== index
    );
  });
  if (duplicate) throw new Error('Duplicate tool key: ' + duplicate.toolKey);
  return definitions;
}

function captureEngineConfig(filePath) {
  const source = fs.readFileSync(filePath, 'utf8');
  const localRequire = createRequire(filePath);
  let captured = null;
  const sandbox = { module: { exports: {} }, exports: {}, console, Math };
  sandbox.exports = sandbox.module.exports;
  sandbox.require = function (request) {
    if (request === './_factory') {
      return {
        createEngine: function (config) {
          captured = config;
          return Object.assign({}, config, {
            calculate: function () {
              return {};
            },
          });
        },
        calcProgressiveBands: function () {
          return { tax: 0, details: [] };
        },
        calcCumulativeBands: function () {
          return { tax: 0, details: [] };
        },
      };
    }
    return localRequire(request);
  };
  vm.runInNewContext(source, sandbox, { filename: filePath });
  if (!captured) throw new Error('Could not capture createEngine config from ' + filePath);
  return captured;
}

function captureConstants(filePath, names) {
  const source = fs.readFileSync(filePath, 'utf8');
  const localRequire = createRequire(filePath);
  const sandbox = { module: { exports: {} }, exports: {}, require: localRequire, console, Math };
  sandbox.exports = sandbox.module.exports;
  const exportExpression = names
    .map(function (name) {
      return JSON.stringify(name) + ': ' + name;
    })
    .join(', ');
  vm.runInNewContext(source + '\nmodule.exports.__aiContextFacts = {' + exportExpression + '};', sandbox, { filename: filePath });
  return sandbox.module.exports.__aiContextFacts;
}

function finiteOrNull(value) {
  return Number.isFinite(value) ? Number(value) : null;
}

function normalizeBands(rows, mode) {
  let cursor = 0;
  return (rows || []).map(function (row) {
    const boundary = finiteOrNull(Number(row[0]));
    const rate = Number(row[1]);
    let from = cursor;
    let to = null;
    if (mode === 'threshold') {
      to = boundary;
      if (boundary !== null) cursor = boundary;
    } else {
      to = boundary === null ? null : cursor + boundary;
      if (boundary !== null) cursor += boundary;
    }
    return { from, to, rate };
  });
}

function optionDeductions(engine) {
  if (!engine || typeof engine.getOptions !== 'function') return [];
  const options = engine.getOptions() || {};
  return (options.deductions || []).map(function (item) {
    const match = String(item.label || '').match(/([0-9]+(?:\.[0-9]+)?)%/);
    return { key: item.key || '', label: item.label || item.key || '', rate: match ? Number(match[1]) / 100 : null };
  });
}

