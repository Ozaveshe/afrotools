function buildContext(definition) {
  const pieces = [];
  if (definition.staticText) pieces.push(definition.staticText.trim());
  let sourceRecord = null;
  if (definition.status === 'source-coupled') {
    sourceRecord = buildSourceRecord(definition.sourceBindings[0]);
    pieces.push(sourceRecord.text);
  }
  for (const block of definition.unverifiedStaticBlocks || []) {
    pieces.push('Unverified static guidance (not source-coupled): ' + block.text.trim());
  }
  return { text: pieces.filter(Boolean).join(' '), sourceRecord };
}

function buildAll() {
  const definitions = loadDefinitions();
  const contexts = {};
  const records = {};
  let coupled = 0;
  let unverified = 0;
  for (const definition of definitions) {
    const built = buildContext(definition);
    contexts[definition.toolKey] = built.text;
    records[definition.toolKey] = { definition, sourceRecord: built.sourceRecord };
    if (definition.status === 'source-coupled') coupled += 1;
    else unverified += 1;
  }
  return { contexts, records, summary: { total: definitions.length, sourceCoupled: coupled, unverifiedStatic: unverified } };
}

function main() {
  const bootstrap = process.argv.includes('--bootstrap');
  const rewriteAdvisor = process.argv.includes('--rewrite-advisor');
  const write = process.argv.includes('--write') || bootstrap;
  const check = process.argv.includes('--check');
  if (bootstrap) {
    const count = bootstrapDefinitions();
    console.log('Bootstrapped ' + count + ' per-tool AI context definitions.');
  }

  if (rewriteAdvisor) {
    rewriteAdvisorImport();
    console.log('Replaced inline TOOL_CONTEXT with the generated module import.');
  }

  const built = buildAll();
  const output = generatedModule(built.contexts);
  if (write) {
    atomicWrite(GENERATED_PATH, output);
    console.log('Built AI tool context: ' + built.summary.total + ' tools; ' + built.summary.sourceCoupled + ' source-coupled; ' + built.summary.unverifiedStatic + ' unverified-static.');
    return;
  }
  if (check) {
    if (!fs.existsSync(GENERATED_PATH) || fs.readFileSync(GENERATED_PATH, 'utf8') !== output) {
      console.error('ERROR netlify/functions/_shared/ai-tool-context.generated.js is stale. Run npm run ai:tool-context.');
      process.exitCode = 1;
      return;
    }
    console.log('AI tool context is current: ' + built.summary.total + ' tools.');
    return;
  }
  console.log(JSON.stringify(built.summary, null, 2));
}

