function genericPayeFacts(filePath) {
  const config = captureEngineConfig(filePath);
  const mode = config.bandType === 'cumulative' ? 'threshold' : 'width';
  return {
    period: config.isMonthly ? 'monthly' : 'annual',
    bands: normalizeBands(config.bands, mode),
    deductions: (config.socialSecurity || []).map(function (item) {
      return {
        key: item.key || '',
        label: item.label || item.key || '',
        rate: typeof item.rate === 'number' ? item.rate : null,
        cap: typeof item.cap === 'number' ? item.cap : null,
      };
    }),
  };
}

function formatNumber(value) {
  return Number(value).toLocaleString('en-US', { maximumFractionDigits: 6 });
}

function formatPercent(rate) {
  return (Number(rate) * 100).toFixed(4).replace(/\.0+$/, '').replace(/(\.\d*?)0+$/, '$1') + '%';
}

function bandText(facts, currency) {
  return facts.bands.map(function (band) {
    const range = band.to === null
      ? 'above ' + currency + ' ' + formatNumber(band.from)
      : currency + ' ' + formatNumber(band.from) + '–' + formatNumber(band.to);
    return formatPercent(band.rate) + ' on ' + range;
  }).join('; ');
}

function deductionText(facts, currency) {
  return (facts.deductions || []).map(function (item) {
    let text = item.label || item.key;
    if (typeof item.rate === 'number' && !/%/.test(text)) text += ' ' + formatPercent(item.rate);
    if (typeof item.cap === 'number') text += ' (cap ' + currency + ' ' + formatNumber(item.cap) + ')';
    if (typeof item.minimum === 'number') text += ' (minimum ' + currency + ' ' + formatNumber(item.minimum) + ')';
    return text;
  }).filter(Boolean).join('; ');
}

function sourceLabel(formula, fallback) {
  const titles = (formula.sources || []).map(function (source) { return source.title; }).filter(Boolean);
  return fallback || titles[0] || formula.id;
}

function payeSourceRecord(binding) {
  const filePath = enginePath(binding.countryCode);
  const source = fs.readFileSync(filePath, 'utf8');
  const engine = require(filePath);
  const formula = formulaById.get(binding.formulaId);
  if (!formula) throw new Error('Missing formula ' + binding.formulaId);
  const direct = directPayeFacts(binding.countryCode, filePath, engine);
  const facts = direct || genericPayeFacts(filePath);
  facts.countryCode = binding.countryCode;
  facts.currency = engine.currency || formula.currency;
  facts.engineDigest = sha256(source);
  facts.formulaId = formula.id;
  facts.formulaVersion = formula.formulaVersion;
  facts.asOf = engine.lastUpdated || formula.lastVerified;
  facts.sourceCheckedOn = engine.sourceCheckedOn || null;
  facts.sourceLabel = sourceLabel(formula, engine.source);

  let text = 'Current structured PAYE facts (' + facts.period + '): ' + bandText(facts, facts.currency) + '.';
  const deductions = deductionText(facts, facts.currency);
  if (deductions) text += ' Structured deductions: ' + deductions + '.';
  text += ' Source: ' + facts.sourceLabel + '. As of: ' + facts.asOf + '. Formula: ' + facts.formulaId + ' (' + facts.formulaVersion + ').';
  return { text, facts };
}

function vatSourceRecord(binding) {
  const formula = formulaById.get(binding.formulaId);
  if (!formula) throw new Error('Missing formula ' + binding.formulaId);
  const artifactPath = path.join(ROOT, formula.artifactPath);
  const source = fs.readFileSync(artifactPath, 'utf8');
  const match = source.match(/\b(?:RATE|DEFAULT_RATE|currentRate)\s*=\s*([0-9]+(?:\.[0-9]+)?)/);
  if (!match) throw new Error('Could not find structured VAT RATE in ' + formula.artifactPath);
  const facts = {
    countryCode: binding.countryCode,
    currency: formula.currency,
    standardRate: Number(match[1]),
    formulaId: formula.id,
    formulaVersion: formula.formulaVersion,
    artifactDigest: formula.artifactDigest,
    asOf: formula.lastVerified,
    sourceLabel: sourceLabel(formula),
  };
  const text = 'Current structured VAT facts: standard rate ' + formatPercent(facts.standardRate) + '. Source: ' + facts.sourceLabel + '. As of: ' + facts.asOf + '. Formula: ' + facts.formulaId + ' (' + facts.formulaVersion + ').';
  return { text, facts };
}

function fuelSourceRecord() {
  const meta = readJson(META_PATH).fuel;
  const snapshot = readJson(FUEL_PATH);
  const selected = ['NG', 'KE', 'ZA', 'GH'].map(function (code) {
    const row = snapshot.countries.find(function (country) { return country.code === code; });
    if (!row) throw new Error('Fuel snapshot missing ' + code);
    return {
      code: row.code,
      name: row.name,
      currency: row.currency,
      petrol: row.petrol && row.petrol.price,
      diesel: row.diesel && row.diesel.price,
      asOf: row.last_updated,
    };
  });
  const facts = {
    dataset: 'fuel',
    sourceLabel: meta.source || snapshot.source,
    sourceState: meta.source_state || 'third_party_snapshot',
    asOf: meta.as_of || meta.last_fetch,
    countries: selected,
  };
  const rates = selected.map(function (row) {
    return row.name + ': petrol ' + row.currency + ' ' + formatNumber(row.petrol) + '/litre, diesel ' + row.currency + ' ' + formatNumber(row.diesel) + '/litre';
  }).join('; ');
  const text = 'Current structured fuel snapshot: ' + rates + '. Source: ' + facts.sourceLabel + ' (' + facts.sourceState + ', not official verification). As of: ' + facts.asOf + '.';
  return { text, facts };
}

function remittanceSourceRecord() {
  const meta = readJson(META_PATH).forex;
  const snapshot = readJson(FOREX_PATH);
  const engine = require(path.join(ROOT, 'assets', 'js', 'engines', 'remittance-v2.js'));
  const currencies = ['NGN', 'KES', 'GHS', 'ZAR'].map(function (currency) {
    return { currency, unitsPerUsd: Number(snapshot.rates[currency]) };
  });
  const providers = Object.keys(engine.PROVIDER_TYPES).sort().map(function (key) {
    const provider = engine.PROVIDER_TYPES[key];
    return {
      key,
      feeRate: provider.defaultFeePct,
      marginPercent: provider.defaultMarginPct,
      speedHours: provider.speedHours,
    };
  });
  const facts = {
    dataset: 'forex',
    base: snapshot.base,
    sourceLabel: meta.source || snapshot.source,
    asOf: meta.as_of || snapshot.timestamp,
    currencies,
    providers,
  };
  const rates = currencies.map(function (item) {
    return '1 USD = ' + formatNumber(item.unitsPerUsd) + ' ' + item.currency;
  }).join('; ');
  const providerText = providers.map(function (provider) {
    return provider.key + ' fee ' + formatPercent(provider.feeRate) + ', FX margin ' + formatNumber(provider.marginPercent) + '%, speed ' + formatNumber(provider.speedHours) + 'h';
  }).join('; ');
  const text = 'Current structured remittance assumptions: ' + rates + '. Engine provider defaults: ' + providerText + '. Source: ' + facts.sourceLabel + '. As of: ' + facts.asOf + '.';
  return { text, facts };
}

