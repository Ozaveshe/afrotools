function frenchToolSlugToEnglishSource(frSlug) {
  const mapped = FRENCH_TOOL_SLUG_TO_ENGLISH_TOOL[frSlug];
  return mapped ? `tools/${mapped}` : null;
}

function frenchRouteForEnglishToolSource(source) {
  const clean = String(source || "").replace(/^\/+|\/+$/g, "");
  const match = clean.match(/^tools\/([^/]+)$/);
  if (!match) return null;

  const frSlug = ENGLISH_TOOL_TO_FRENCH_SLUG[match[1]];
  return frSlug ? `/fr/tools/${frSlug}` : null;
}

