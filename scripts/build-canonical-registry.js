#!/usr/bin/env node
'use strict';

const fs = require('fs');
const path = require('path');
const api = require('./lib/canonical-registry');

const ROOT = api.ROOT;
const CANONICAL_PATH = path.join(ROOT, 'data', 'registry', 'canonical-registry.json');
const REPORT_JSON_PATH = path.join(ROOT, 'reports', 'canonical-registry-report.json');
const REPORT_MD_PATH = path.join(ROOT, 'reports', 'canonical-registry-report.md');
const CLIENT_COUNTS_PATH = path.join(ROOT, 'assets', 'js', 'data', 'registry-counts.js');
const BROWSER_COUNTRIES_PATH = path.join(ROOT, 'assets', 'js', 'data', 'african-countries.js');
const POLICY_PATH = path.join(ROOT, 'data', 'registry', 'catalog-policy.json');

function stableJson(value) {
  return `${JSON.stringify(value, null, 2)}\n`;
}

function reportMarkdown(report) {
  const rows = [
    ['Raw tool registry rows', report.summary.rawToolRows],
    ['Explicit redirect aliases', report.summary.redirectAliases],
    ['Canonical published tool records', report.summary.canonicalPublishedTools],
    ['Canonical published English tool records', report.summary.canonicalEnglishTools],
    ['Localized published tool records', report.summary.localizedTools],
    ['Expanded live tool experiences', report.summary.expandedLiveToolExperiences],
    ['Indexable tool destinations', report.summary.indexableToolDestinations],
    ['Widget-enabled tool records', report.summary.widgetEnabledTools],
    ['Published widgets', report.summary.publishedWidgets],
    ['Widget categories', report.summary.widgetCategories],
    ['Published tool categories', report.summary.categories],
    ['Published African country hubs', report.summary.countries],
    ['Published site languages', report.summary.siteLanguages],
    ['API plans', report.summary.apiPlans],
    ['Product subscription options', report.summary.productPlanOptions],
    ['Pro apps', report.summary.proApps]
  ];
  const lines = [
    '# Canonical Registry Report',
    '',
    'This file is generated by `node scripts/build-canonical-registry.js --write`.',
    'Counts with different membership rules intentionally use different labels.',
    '',
    '## Summary',
    '',
    '| Semantic count | Value |',
    '|---|---:|',
    ...rows.map(([label, value]) => `| ${label} | ${value} |`),
    '',
    '## Definitions',
    '',
    ...Object.entries(report.definitions).map(([id, definition]) => `- **${id}:** ${definition}`),
    '',
    '## Explicit route aliases',
    '',
    '| Alias record | Route | Canonical record |',
    '|---|---|---|',
    ...report.aliases.map((entry) => `| ${entry.id} | ${entry.route} | ${entry.redirectTarget.toolId} |`),
    '',
    '## Locale coverage',
    '',
    '| Locale | State | Published tool records |',
    '|---|---|---:|',
    ...report.localeCoverage.map((entry) => `| ${entry.id} | ${entry.state} | ${entry.toolRecords} |`),
    '',
    '## Category coverage',
    '',
    '| Category | Canonical published tool records |',
    '|---|---:|',
    ...report.categoryCoverage.map((entry) => `| ${entry.id} | ${entry.toolRecords} |`),
    '',
    '## Named selectors',
    '',
    '| Selector | Label | Value | Definition |',
    '|---|---|---:|---|',
    ...report.selectors.map((entry) => `| ${entry.id} | ${entry.label} | ${entry.value} | ${entry.definition.replace(/\|/g, '\\|')} |`),
    ''
  ];
  return lines.join('\n');
}

function clientCountsScript(registry) {
  const counts = Object.fromEntries(registry.selectors.map((item) => [item.id, item.value]));
  return `(function(root){'use strict';root.AFRO_REGISTRY_COUNTS=${JSON.stringify(counts)};})(typeof window!=='undefined'?window:globalThis);\n`;
}

function browserCountriesScript(registry) {
  const records = registry.countries.map((country) => ({
    code: country.id,
    name: country.title,
    displayNames: country.displayNames,
    slug: country.routeSlug,
    route: country.route,
    region: country.region,
    currency: country.currency,
    currencySymbol: country.currencySymbol,
    flag: country.flag,
    sourceJurisdiction: country.sourceJurisdiction,
    supportedToolTypes: country.supportedToolTypes,
    localeCoverage: country.localeCoverage,
    languages: country.languages.join(', ')
  }));
  return `(function(root){'use strict';root.AFRICAN_COUNTRIES=${JSON.stringify(records)};})(typeof window!=='undefined'?window:globalThis);\n`;
}

function replaceCountMarkers(content, file, registry) {
  const marker = /(<[^>]+\bdata-registry-count="([^"]+)"[^>]*>)([^<]*)(<\/[^>]+>)/g;
  const seen = new Map();
  let updated = content.replace(marker, (match, open, selectorId, current, close) => {
    const count = (seen.get(selectorId) || 0) + 1;
    seen.set(selectorId, count);
    const selected = api.getSelector(registry, selectorId);
    if (!selected) throw new Error(`${file}: unknown data-registry-count selector ${selectorId}`);
    return `${open}${selected.value}${close}`;
  });
  const plans = new Map([...registry.productPlans, ...registry.apiPlans].map((plan) => [plan.id, plan]));
  const planMarker = /(<[^>]+\bdata-registry-plan="([^"]+)"[^>]+\bdata-registry-field="([^"]+)"[^>]*>)([^<]*)(<\/[^>]+>)/g;
  updated = updated.replace(planMarker, (match, open, planId, field, current, close) => {
    const plan = plans.get(planId);
    if (!plan) throw new Error(`${file}: unknown data-registry-plan ${planId}`);
    if (!['title', 'suffix', 'detail', 'currency', 'interval'].includes(field)) throw new Error(`${file}: unsupported plan field ${planId}.${field}`);
    if (plan[field] === undefined || plan[field] === null) throw new Error(`${file}: missing plan field ${planId}.${field}`);
    const key = `plan:${planId}.${field}`;
    seen.set(key, (seen.get(key) || 0) + 1);
    return `${open}${plan[field]}${close}`;
  });
  return { content: updated, markerCount: [...seen.values()].reduce((sum, count) => sum + count, 0), seen };
}

function syncCountSurfaces(registry, write) {
  const policy = JSON.parse(fs.readFileSync(POLICY_PATH, 'utf8'));
  const stale = [];
  let markerCount = 0;
  policy.countSurfaceFiles.forEach((relative) => {
    const absolute = path.join(ROOT, relative);
    if (!fs.existsSync(absolute)) throw new Error(`${relative}: declared count surface does not exist`);
    const before = fs.readFileSync(absolute, 'utf8');
    const result = replaceCountMarkers(before, relative, registry);
    markerCount += result.markerCount;
    if (before !== result.content) {
      stale.push(relative);
      if (write) fs.writeFileSync(absolute, result.content, 'utf8');
    }
  });
  if (!markerCount) throw new Error('No data-registry-count markers were found in declared count surfaces.');
  return { stale, markerCount };
}

function writeOrCheck(file, expected, write, stale) {
  const current = fs.existsSync(file) ? fs.readFileSync(file, 'utf8') : null;
  if (current === expected) return;
  stale.push(path.relative(ROOT, file).replace(/\\/g, '/'));
  if (write) {
    fs.mkdirSync(path.dirname(file), { recursive: true });
    fs.writeFileSync(file, expected, 'utf8');
  }
}

function run(options) {
  const write = Boolean(options && options.write);
  const registry = api.buildCanonicalRegistry();
  const validation = api.validateCanonicalRegistry(registry);
  if (!validation.ok) {
    validation.errors.forEach((item) => console.error(api.formatIssue(item)));
    throw new Error(`Canonical registry validation failed with ${validation.errors.length} error(s).`);
  }

  const report = api.buildRegistryReport(registry);
  const stale = [];
  writeOrCheck(CANONICAL_PATH, stableJson(registry), write, stale);
  writeOrCheck(REPORT_JSON_PATH, stableJson(report), write, stale);
  writeOrCheck(REPORT_MD_PATH, reportMarkdown(report), write, stale);
  writeOrCheck(CLIENT_COUNTS_PATH, clientCountsScript(registry), write, stale);
  writeOrCheck(BROWSER_COUNTRIES_PATH, browserCountriesScript(registry), write, stale);
  const surfaces = syncCountSurfaces(registry, write);
  surfaces.stale.forEach((file) => {
    if (!stale.includes(file)) stale.push(file);
  });

  if (!write && stale.length) {
    throw new Error(`Canonical registry outputs are stale:\n- ${stale.join('\n- ')}\nRun node scripts/build-canonical-registry.js --write.`);
  }

  console.log(`Canonical registry valid: ${registry.tools.length} tool rows, ${report.summary.canonicalPublishedTools} canonical published tools, ${report.summary.publishedWidgets} widgets.`);
  console.log(`${write ? 'Synchronized' : 'Verified'} ${surfaces.markerCount} named HTML count markers.`);
  if (write && stale.length) console.log(`Updated ${stale.length} generated or synchronized file(s).`);
  return { registry, report, stale, surfaces };
}

function main(argv) {
  const args = argv || process.argv.slice(2);
  const write = args.includes('--write');
  if (args.includes('--check') && write) throw new Error('Choose either --check or --write, not both.');
  return run({ write });
}

if (require.main === module) {
  try {
    main();
  } catch (error) {
    console.error(error.message);
    process.exit(1);
  }
}

module.exports = { run, main, replaceCountMarkers, syncCountSurfaces, reportMarkdown, clientCountsScript, browserCountriesScript };
