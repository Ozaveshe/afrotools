#!/usr/bin/env node
/**
 * Builds a lightweight dataset for the related-tools component so pages
 * don't need the full tool registry just to render recommendations.
 */
const fs = require('fs');
const path = require('path');
const vm = require('vm');

const ROOT = path.resolve(__dirname, '..');
const REGISTRY_PATH = path.join(ROOT, 'assets', 'js', 'components', 'tool-registry.js');
const OUTPUT_PATH = path.join(ROOT, 'assets', 'js', 'components', 'related-tools-data.js');
const OUTPUT_MIN_PATH = path.join(ROOT, 'assets', 'js', 'components', 'related-tools-data.min.js');

const registrySource = fs.readFileSync(REGISTRY_PATH, 'utf8');
const context = {
  console,
  setTimeout,
  clearTimeout,
};
vm.createContext(context);
vm.runInContext(registrySource, context, { filename: 'tool-registry.js' });

const allTools = Array.isArray(context.AFRO_TOOLS) ? context.AFRO_TOOLS : [];
const liveTools = allTools
  .filter((tool) => tool && typeof tool === 'object' && typeof tool.id === 'string' && tool.status === 'live')
  .map((tool) => ({
    id: tool.id,
    name: tool.name,
    icon: tool.icon || '',
    desc: (tool.desc || '').length > 50 ? `${tool.desc.slice(0, 48)}...` : (tool.desc || ''),
    href: tool.href || '#',
    category: tool.category || '',
    lang: tool.lang || 'en',
    priority: tool.priority || 0,
    estTraffic: tool.estTraffic || 0,
    imageExt: (context.TOOL_CARD_IMAGE_EXTENSIONS && context.TOOL_CARD_IMAGE_EXTENSIONS[tool.id]) || '',
  }));

const sortByPriority = (a, b) => (b.priority - a.priority) || (b.estTraffic - a.estTraffic) || a.name.localeCompare(b.name);
const sortByTraffic = (a, b) => (b.estTraffic - a.estTraffic) || (b.priority - a.priority) || a.name.localeCompare(b.name);

const buckets = {};
const fallback = {};

for (const tool of liveTools) {
  const lang = tool.lang || 'en';
  const bucketKey = `${lang}::${tool.category || ''}`;
  if (!buckets[bucketKey]) buckets[bucketKey] = [];
  buckets[bucketKey].push(tool);
  if (!fallback[lang]) fallback[lang] = [];
  fallback[lang].push(tool);
}

for (const key of Object.keys(buckets)) {
  buckets[key] = buckets[key].sort(sortByPriority).slice(0, 18);
}

for (const lang of Object.keys(fallback)) {
  fallback[lang] = fallback[lang].sort(sortByTraffic).slice(0, 36);
}

const relatedTools = {
  version: 1,
  buckets,
  fallback,
};

const banner = '// Generated by scripts/build-related-tools-data.js\n';
const prettyPayload = banner +
  `(function(root){root.AFRO_RELATED_TOOLS=${JSON.stringify(relatedTools, null, 2)};})(typeof window!=="undefined"?window:globalThis);\n`;
const compactPayload = banner +
  `(function(root){root.AFRO_RELATED_TOOLS=${JSON.stringify(relatedTools)};})(typeof window!=="undefined"?window:globalThis);`;

fs.writeFileSync(OUTPUT_PATH, prettyPayload, 'utf8');
fs.writeFileSync(OUTPUT_MIN_PATH, compactPayload, 'utf8');

const prettySize = Buffer.byteLength(prettyPayload);
const compactSize = Buffer.byteLength(compactPayload);
console.log(`  RELATED ${liveTools.length} live tools compressed into recommendation buckets`);
console.log(`          ${path.relative(ROOT, OUTPUT_PATH)} ${prettySize} bytes`);
console.log(`          ${path.relative(ROOT, OUTPUT_MIN_PATH)} ${compactSize} bytes`);
