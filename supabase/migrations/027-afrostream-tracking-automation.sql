-- AfroStream tracking automation
-- Adds durable sources for supporter rows and RSS/news mention ingestion.

ALTER TABLE public.as_news
  ADD COLUMN IF NOT EXISTS source_url TEXT,
  ADD COLUMN IF NOT EXISTS source_name TEXT,
  ADD COLUMN IF NOT EXISTS external_id TEXT;

CREATE UNIQUE INDEX IF NOT EXISTS as_news_external_id_uidx
  ON public.as_news (external_id)
  WHERE external_id IS NOT NULL;

CREATE TABLE IF NOT EXISTS public.as_creator_supporters (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  creator_id BIGINT REFERENCES public.as_creators(id) ON DELETE CASCADE,
  supporter_name TEXT NOT NULL,
  platform TEXT,
  amount NUMERIC DEFAULT 0,
  currency TEXT DEFAULT 'USD',
  source_label TEXT,
  source_url TEXT,
  event_date DATE DEFAULT CURRENT_DATE,
  is_verified BOOLEAN DEFAULT false,
  is_published BOOLEAN DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS as_creator_supporters_creator_idx
  ON public.as_creator_supporters (creator_id, is_published, amount DESC, event_date DESC);

CREATE TABLE IF NOT EXISTS public.as_news_sources (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  feed_url TEXT NOT NULL UNIQUE,
  category TEXT DEFAULT 'creator-news',
  is_active BOOLEAN DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS public.as_news_creator_mentions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  news_id BIGINT REFERENCES public.as_news(id) ON DELETE CASCADE,
  creator_id BIGINT REFERENCES public.as_creators(id) ON DELETE CASCADE,
  matched_name TEXT,
  source_url TEXT,
  detected_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (news_id, creator_id)
);

CREATE INDEX IF NOT EXISTS as_news_creator_mentions_creator_idx
  ON public.as_news_creator_mentions (creator_id, detected_at DESC);

ALTER TABLE public.as_creator_supporters ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.as_news_sources ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.as_news_creator_mentions ENABLE ROW LEVEL SECURITY;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'as_creator_supporters'
      AND policyname = 'Public read published creator supporters'
  ) THEN
    CREATE POLICY "Public read published creator supporters"
      ON public.as_creator_supporters
      FOR SELECT
      USING (is_published = true);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'as_creator_supporters'
      AND policyname = 'Service full creator supporters'
  ) THEN
    CREATE POLICY "Service full creator supporters"
      ON public.as_creator_supporters
      FOR ALL
      TO service_role
      USING (true)
      WITH CHECK (true);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'as_news_sources'
      AND policyname = 'Public read active news sources'
  ) THEN
    CREATE POLICY "Public read active news sources"
      ON public.as_news_sources
      FOR SELECT
      USING (is_active = true);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'as_news_sources'
      AND policyname = 'Service full news sources'
  ) THEN
    CREATE POLICY "Service full news sources"
      ON public.as_news_sources
      FOR ALL
      TO service_role
      USING (true)
      WITH CHECK (true);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'as_news_creator_mentions'
      AND policyname = 'Public read news creator mentions'
  ) THEN
    CREATE POLICY "Public read news creator mentions"
      ON public.as_news_creator_mentions
      FOR SELECT
      USING (true);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'as_news_creator_mentions'
      AND policyname = 'Service full news creator mentions'
  ) THEN
    CREATE POLICY "Service full news creator mentions"
      ON public.as_news_creator_mentions
      FOR ALL
      TO service_role
      USING (true)
      WITH CHECK (true);
  END IF;
END $$;
