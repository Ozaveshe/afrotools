const fs = require('fs');
const pdfParse = require('pdf-parse');
const { test, expect } = require('@playwright/test');

test('investment return VIP is exact, local-first and responsive', async ({ page }) => {
  const errors = [];
  const networkBodies = [];
  page.on('console', message => { if (message.type() === 'error') errors.push(message.text()); });
  page.on('pageerror', error => errors.push(error.message));
  page.on('request', request => {
    if (request.method() !== 'GET') networkBodies.push(request.postData() || '');
  });
  await page.setViewportSize({ width: 320, height: 760 });
  await page.emulateMedia({ colorScheme: 'dark', reducedMotion: 'reduce' });
  await page.goto('/tools/investment-return/', { waitUntil: 'domcontentloaded' });
  await expect(page.locator('h1')).toContainText('inflation');
  await page.fill('#ir-initial', '1000');
  await page.fill('#ir-monthly', '100');
  await page.fill('#ir-rate', '12');
  await page.fill('#ir-years', '1');
  await page.selectOption('#ir-compound', '12');
  await page.selectOption('#ir-timing', 'end');
  await page.fill('#ir-inflation', '6');
  await page.click('#ir-form button[type="submit"]');
  await expect(page.locator('#ir-final')).toContainText(/2[,.\s\u00a0]*395/);
  await expect(page.locator('#ir-metrics')).toContainText(/12[,.]68%/);
  await expect(page.locator('#ir-metrics')).toContainText(/6[,.]30%/);
  await expect(page.locator('#ir-sensitivity .ir-scenario')).toHaveCount(3);
  const csvPromise = page.waitForEvent('download');
  await page.click('#ir-csv');
  const csv = fs.readFileSync(await (await csvPromise).path(), 'utf8');
  expect(csv).toContain('"Contribution timing","end of month"');
  expect(csv).toContain('"Year 1","2200.00","195.08","2395.08"');
  const pdfPromise = page.waitForEvent('download');
  await page.click('#ir-pdf');
  const pdf = await pdfParse(fs.readFileSync(await (await pdfPromise).path()));
  expect(pdf.text).toContain('Investment Return Projection');
  expect(pdf.text).toContain('Initial investment');
  expect(pdf.text).toMatch(/1,?000\.00/);
  expect(pdf.text).toContain('Compounding frequency');
  await page.fill('#ir-monthly', '0');
  await page.fill('#ir-rate', '-10');
  await page.selectOption('#ir-compound', '1');
  await page.click('#ir-form button[type="submit"]');
  await expect(page.locator('#ir-final')).toContainText(/900[,.\s\u00a0]*00/);
  await expect(page.locator('#ir-metrics .ir-negative').first()).toContainText(/-.*100[,.\s\u00a0]*00/);
  expect((await page.locator('#ir-chart polyline').getAttribute('points')).trim().split(/\s+/)).toHaveLength(2);
  expect(await page.locator('.ir-field input').evaluateAll(inputs => inputs.every(input => input.labels && input.labels.length > 0))).toBe(true);
  expect(await page.evaluate(() => document.documentElement.scrollWidth > document.documentElement.clientWidth + 1)).toBe(false);
  await page.setViewportSize({ width: 360, height: 800 });
  expect(await page.evaluate(() => document.documentElement.scrollWidth > document.documentElement.clientWidth + 1)).toBe(false);
  await page.setViewportSize({ width: 375, height: 812 });
  expect(await page.evaluate(() => document.documentElement.scrollWidth > document.documentElement.clientWidth + 1)).toBe(false);
  await page.screenshot({ path: 'artifacts/investment-return-vip-375-dark.png', fullPage: true });
  await page.setViewportSize({ width: 768, height: 900 });
  expect(await page.evaluate(() => document.documentElement.scrollWidth > document.documentElement.clientWidth + 1)).toBe(false);
  await page.evaluate(() => { document.documentElement.style.fontSize = '200%'; });
  expect(await page.evaluate(() => document.documentElement.scrollWidth > document.documentElement.clientWidth + 1)).toBe(false);
  expect(networkBodies.join('\n')).not.toContain('1000');
  expect(await page.locator('body').innerText()).not.toContain('Get AI Investment Analysis');
  expect(await page.evaluate(() => Object.keys(localStorage).filter(key => /investment|return/i.test(key)))).toEqual([]);
  expect(errors).toEqual([]);
});

test('owned investment widget uses shared compounding and reports failures', async ({ page }) => {
  const errors = [];
  page.on('pageerror', error => errors.push(error.message));
  await page.setViewportSize({ width: 360, height: 700 });
  await page.goto('/widgets/iframe/financial-investment-return.html?theme=dark', { waitUntil: 'domcontentloaded' });
  await page.fill('#aw-initial', '1000');
  await page.fill('#aw-monthly', '0');
  await page.fill('#aw-rate', '12');
  await page.fill('#aw-years', '1');
  await page.selectOption('#aw-compound', '1');
  await page.selectOption('#aw-timing', 'beginning');
  await page.fill('#aw-inflation', '6');
  await page.click('#aw-calc');
  await expect(page.locator('#aw-res')).toContainText('1,120.00');
  await expect(page.locator('#aw-res')).toContainText('5.66%');
  await page.fill('#aw-initial', '0');
  await page.click('#aw-calc');
  await expect(page.locator('#aw-status')).toContainText('initial investment or monthly contribution');
  expect(await page.evaluate(() => document.documentElement.scrollWidth > document.documentElement.clientWidth + 1)).toBe(false);
  expect(errors).toEqual([]);
});
