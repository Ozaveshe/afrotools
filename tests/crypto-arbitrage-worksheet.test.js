'use strict';
const assert=require('node:assert/strict');
const engine=require('../assets/js/pages/crypto-arbitrage-worksheet.js');
const now=Date.parse('2026-07-23T12:00:00Z');
const base={assetCode:'USDT',assetAmount:10,buyDebit:100000,sellCredit:105000,externalCosts:1000,buyChecked:'2026-07-23T10:00:00Z',sellChecked:'2026-07-23T10:01:00Z',buyExpiry:'2026-07-23T13:00:00Z',sellExpiry:'2026-07-23T13:00:00Z',confirmed:true};
const result=engine.calculate(base,now);
assert.equal(result.ok,true);
assert.equal(result.grossDifference,5000);
assert.equal(result.netDifference,4000);
assert.equal(result.returnPct,4);
assert.equal(result.breakEvenSellCredit,101000);
assert.equal(engine.calculate({...base,assetCode:''},now).code,'invalid');
assert.equal(engine.calculate({...base,assetAmount:0},now).code,'invalid');
assert.equal(engine.calculate({...base,externalCosts:-1},now).code,'invalid');
assert.equal(engine.calculate({...base,buyChecked:'2026-07-23T12:01:00Z'},now).code,'future');
assert.equal(engine.calculate({...base,buyExpiry:'2026-07-23T09:00:00Z'},now).code,'order');
assert.equal(engine.calculate({...base,buyExpiry:'2026-07-23T11:00:00Z'},now).code,'expired');
assert.equal(engine.calculate({...base,confirmed:false},now).code,'confirm');
assert.equal(engine.calculate({...base,buyExpiry:''},now).unknownExpiry,true);
assert.equal(engine.safeCsv('=1+1'),'"\'=1+1"');
assert.equal(engine.safeCsv('safe'),'"safe"');
console.log('crypto arbitrage worksheet unit contract: PASS');
