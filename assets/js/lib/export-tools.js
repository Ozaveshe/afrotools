/**
 * AfroTools — Data Export Library
 * Adds CSV + PDF export buttons to any tool result container.
 *
 * Usage:
 *   <script src="/assets/js/lib/export-tools.js"></script>
 *   Then call:
 *     AfroExport.csv(data, filename)
 *     AfroExport.pdf(elementId, filename)
 *     AfroExport.mountButtons(containerId, data, filename)
 */

(function() {
  'use strict';

  var AfroExport = {
    /**
     * Export data as CSV file download.
     * @param {Array} data - Array of objects (each object = one row)
     * @param {string} [filename] - Download filename
     */
    csv: function(data, filename) {
      if (!Array.isArray(data) || data.length === 0) return;
      filename = filename || 'afrotools-export.csv';

      var headers = Object.keys(data[0]);
      var rows = [headers.join(',')];

      data.forEach(function(row) {
        var values = headers.map(function(h) {
          var val = row[h];
          if (val === null || val === undefined) return '';
          var str = String(val);
          // Escape commas and quotes
          if (str.includes(',') || str.includes('"') || str.includes('\n')) {
            return '"' + str.replace(/"/g, '""') + '"';
          }
          return str;
        });
        rows.push(values.join(','));
      });

      var csvContent = rows.join('\n');
      var blob = new Blob([csvContent], { type: 'text/csv;charset=utf-8;' });
      downloadBlob(blob, filename);
    },

    /**
     * Export a DOM element as PDF using print.
     * @param {string} elementId - ID of element to print
     * @param {string} [title] - Document title
     */
    pdf: function(elementId, title) {
      var el = document.getElementById(elementId);
      if (!el) return;

      title = title || 'AfroTools Export';
      var printWindow = window.open('', '_blank');
      printWindow.document.write(
        '<!DOCTYPE html><html><head><title>' + title + '</title>' +
        '<style>' +
          'body{font-family:-apple-system,system-ui,sans-serif;padding:24px;color:#0f172a}' +
          'table{width:100%;border-collapse:collapse;font-size:13px}' +
          'th,td{padding:8px 12px;border:1px solid #e2e8f0;text-align:left}' +
          'th{background:#007AFF;color:#fff;font-weight:700}' +
          '.branding{text-align:center;margin-top:24px;font-size:11px;color:#64748b}' +
        '</style></head><body>' +
        '<h2 style="color:#007AFF">' + title + '</h2>' +
        '<p style="font-size:12px;color:#64748b">Generated by AfroTools on ' + new Date().toLocaleDateString() + '</p>' +
        el.innerHTML +
        '<div class="branding">afrotools.com — Africa\'s #1 Free Tools Platform</div>' +
        '</body></html>'
      );
      printWindow.document.close();
      printWindow.print();
    },

    /**
     * Export data as JSON file download.
     * @param {object} data - Any JSON-serializable data
     * @param {string} [filename]
     */
    json: function(data, filename) {
      filename = filename || 'afrotools-export.json';
      var blob = new Blob([JSON.stringify(data, null, 2)], { type: 'application/json' });
      downloadBlob(blob, filename);
    },

    /**
     * Mount export buttons into a container.
     * @param {string} containerId - ID of container to mount into
     * @param {object} opts - { data, filename, printElementId, title }
     */
    mountButtons: function(containerId, opts) {
      var container = document.getElementById(containerId);
      if (!container) return;

      opts = opts || {};
      var wrapper = document.createElement('div');
      wrapper.className = 'afro-export-bar';
      wrapper.style.cssText = 'display:flex;gap:8px;flex-wrap:wrap;margin:16px 0';

      // CSV button
      if (opts.data) {
        var csvBtn = createButton('CSV', '#34C759', function() {
          AfroExport.csv(opts.data, opts.filename || 'afrotools-data.csv');
        });
        wrapper.appendChild(csvBtn);
      }

      // PDF button
      if (opts.printElementId) {
        var pdfBtn = createButton('PDF', '#007AFF', function() {
          AfroExport.pdf(opts.printElementId, opts.title || 'AfroTools Report');
        });
        wrapper.appendChild(pdfBtn);
      }

      // JSON button
      if (opts.data) {
        var jsonBtn = createButton('JSON', '#FF9500', function() {
          AfroExport.json(opts.data, (opts.filename || 'afrotools-data').replace('.csv', '') + '.json');
        });
        wrapper.appendChild(jsonBtn);
      }

      container.appendChild(wrapper);
    },
  };

  function createButton(label, color, onclick) {
    var btn = document.createElement('button');
    btn.textContent = 'Export ' + label;
    btn.style.cssText = [
      'padding:8px 16px', 'border:1.5px solid ' + color + '40',
      'background:' + color + '10', 'color:' + color,
      'border-radius:8px', 'font-size:12px', 'font-weight:700',
      'cursor:pointer', 'font-family:inherit', 'transition:all .2s',
    ].join(';');
    btn.onmouseover = function() { btn.style.background = color + '20'; };
    btn.onmouseout = function() { btn.style.background = color + '10'; };
    btn.onclick = onclick;
    return btn;
  }

  function downloadBlob(blob, filename) {
    var url = URL.createObjectURL(blob);
    var a = document.createElement('a');
    a.href = url;
    a.download = filename;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  }

  window.AfroExport = AfroExport;
})();
